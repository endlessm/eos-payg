#include "config.h"

#include <glib.h>
#include <gio/gio.h>
#include <libeos-payg/service.h>
#include <locale.h>


/* Test constructing an #EpgService object. Print its address to stop the
 * compiler complaining about an unused variable. */
static void
test_service_construction (void)
{
  g_autoptr(EpgService) service = epg_service_new ();
  g_test_message ("Service constructed as %p", service);
}

int
main (int    argc,
      char **argv)
{
  setlocale (LC_ALL, "");
  g_test_init (&argc, &argv, NULL);

  g_test_add_func ("/service/construction", test_service_construction);

  return g_test_run ();
}
